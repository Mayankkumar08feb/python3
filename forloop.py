#For Loop Task
for i in range(5):
    print(i)
print( " looped completed for i ")
for x in range(1,5):
    print(x)
print( "looped completed for x ")
for X in range(1,5,2):
    print(X)
print(" looped completed for X")
for y in range(5,0,-1):
    print(y)
print( "looped completed for y ")

for z in range(5,0,-2):
    print(z)
print(" looped completed for z ")

